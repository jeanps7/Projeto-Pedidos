    private void btnConsultarPedidoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarPedidoActionPerformed
        int x, itens;
        String aux = "";
        double valorPedido = 0.0;
        for (x = 0; x < cadPed.size(); x++) {
            if (cadPed.get(x) instanceof Pedido) {
                if (((Pedido) cadPed.get(x)).getNumero().equals(txtNumPedido.getText())) {
                    break;
                }
            }
        }

        if (x < cadPed.size()) {
            posPed = x;
        } else {
            posPed = -1;
        }

        if (posPed >= 0) {
            txtData.setText(cadPed.get(posPed).getDataEmissao());
            //btgrFormaPagamento.setSelected(btgrFormaPagamento, cadPed.get(posPed).getFormaPagto());
            rdbPrazo.setSelected(cadPed.get(posPed).getFormaPagto());
            txtCPFCliente.setText(cadPed.get(posPed).getCliente().getCpf());
            lblCliente.setText(cadPed.get(posPed).getCliente().getNome());
            txtCPFVendedor.setText(cadPed.get(posPed).getVendedor().getCpf());
            lblVendedor.setText(cadPed.get(posPed).getVendedor().getNome());
            if (cadPed.get(posPed).getFormaPagto()) {
                rdbVista.setSelected(true);
            } else {
                rdbPrazo.setSelected(true);
            }

            btnConsultarItemPedido.setEnabled(true);

            itens = cadPed.get(posPed).getItemPedido().size();
            for (int i = 0; i < itens; i++) {
                String linhas[] = {cadPed.get(posPed).getItemPedido().get(i).getProduto().getCodigo(),
                    cadPed.get(posPed).getItemPedido().get(i).getProduto().getDescricao(),
                    String.valueOf(cadPed.get(posPed).getItemPedido().get(i).getProduto().getPreco()),
                    String.valueOf(cadPed.get(posPed).getItemPedido().get(i).getQntdVendida()),
                    String.valueOf(cadPed.get(posPed).getItemPedido().get(i).getQntdVendida() * cadPed.get(posPed).getItemPedido().get(i).getProduto().getPreco())};
                modtblProduto.addRow(linhas);
            }

            for (int i = 0; i < modtblProduto.getRowCount(); i++) {
                aux = String.valueOf(modtblProduto.getValueAt(i, 4));
                aux = aux.replace(',', '.');
                valorPedido += Double.parseDouble(aux);
            }

            lblValorTotal.setText(String.valueOf(valorPedido));
            lblQtdeItens.setText(String.valueOf(modtblProduto.getRowCount()));
            txtNumPedido.setEnabled(false);
            btnConsultarPedido.setEnabled(false);
            btnAddItem.setEnabled(true);
            btnRemItem.setEnabled(true);
            btnAlterarPedido.setEnabled(true);
            btnExcluiPedido.setEnabled(true);
            txtCodigoProduto.setEnabled(true);
        } else {
            txtData.setEnabled(true);
            rdbPrazo.setEnabled(true);
            rdbVista.setEnabled(true);
            btnConsultarPedido.setEnabled(false);
            btnConsultarCliente.setEnabled(true);

            txtData.requestFocus();
        }

    }//GEN-LAST:event_btnConsultarPedidoActionPerformed

