    private void btnAddItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddItemActionPerformed
        if (Double.parseDouble(txtQtdeVendida.getText()) > 0 && (!txtQtdeVendida.getText().isEmpty())) {
            if (cadProd.get(posCliVenPro).getQtdeEstoque() >= Double.parseDouble(txtQtdeVendida.getText())) {
                ItemPedido objItem = new ItemPedido(modtblProduto.getRowCount(), Double.parseDouble(txtQtdeVendida.getText()), cadProd.get(posCliVenPro));
                DecimalFormat Formato = new DecimalFormat("#0.0");
                String aux = "";
                double total = (Double.parseDouble(txtQtdeVendida.getText()) * cadProd.get(posCliVenPro).getPreco());;
                double valorPedido = 0.0, qtdVendida = 0.0;
                int x;
                for (x = 0; x < cadCliVend.size(); x++) {
                    if (cadCliVend.get(x) instanceof Cliente) {
                        if (((Cliente) cadCliVend.get(x)).getCpf().equals(txtCPFCliente.getText())) {
                            break;
                        }
                    }
                }

                if (((Cliente) cadCliVend.get(x)).getLimiteDisp() >= total) {
                    objPedido.addItemPedido(objItem);

                    String linha[] = {cadProd.get(posCliVenPro).getCodigo(),
                        cadProd.get(posCliVenPro).getDescricao(),
                        String.valueOf(cadProd.get(posCliVenPro).getPreco())};

                    modtblProduto.addRow(linha);
                    modtblProduto.setValueAt(Formato.format(Double.parseDouble(txtQtdeVendida.getText())), modtblProduto.getRowCount() - 1, 3);
                    modtblProduto.setValueAt(Formato.format(total), modtblProduto.getRowCount() - 1, 4);

                    for (int i = 0; i < modtblProduto.getRowCount(); i++) {
                        aux = String.valueOf(modtblProduto.getValueAt(i, 4));
                        aux = aux.replace(',', '.');
                        valorPedido += Double.parseDouble(aux);
                    }

                    lblValorTotal.setText(String.valueOf(valorPedido));
                    lblQtdeItens.setText(String.valueOf(modtblProduto.getRowCount()));
                    if (!btnAlterarPedido.isEnabled()) {
                        btnIncluirPedido.setEnabled(true);
                    }

                } else {
                    JOptionPane.showMessageDialog(null,
                            "Limite Disponivel Insuficiente!",
                            "Limite Disponivel",
                            JOptionPane.WARNING_MESSAGE);
                    txtQtdeVendida.setText(null);
                    txtQtdeVendida.requestFocus();
                }
            } else {
                JOptionPane.showMessageDialog(null,
                        "Estoque Insuficiente!",
                        "Estoque",
                        JOptionPane.WARNING_MESSAGE);
                txtQtdeVendida.setText(null);
                txtQtdeVendida.requestFocus();
            }
        } else {
            JOptionPane.showMessageDialog(null,
                    "Quantidade precisa ser Maior que 0!",
                    "Quantidade InvÃ¡lida",
                    JOptionPane.WARNING_MESSAGE);
            txtQtdeVendida.setText(null);
            txtQtdeVendida.requestFocus();
        }

    }//GEN-LAST:event_btnAddItemActionPerformed

