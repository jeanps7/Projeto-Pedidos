    private void btnConsultarVendedorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarVendedorActionPerformed
        int x;
        for (x = 0; x < cadCliVend.size(); x++) {
            if (cadCliVend.get(x) instanceof Vendedor) {
                if (((Vendedor) cadCliVend.get(x)).getCpf().equals(txtCPF.getText())) {
                    break;
                }
            }
        }

        if (x < cadCliVend.size()) {
            posCliVenPro = x;
        } else {
            posCliVenPro = -1;
        }

        if (posCliVenPro >= 0) {
            lblVendedor.setText(cadCliVend.get(posCliVenPro).getNome());

            btnConsultar.setEnabled(true);
        } else {
            JOptionPane.showMessageDialog(null,
                    "Vendedor não cadastrado",
                    "Vendedor Inválido",
                    JOptionPane.WARNING_MESSAGE);
            txtCPF.setText(null);
            txtCPF.requestFocus();
        }
    }//GEN-LAST:event_btnConsultarVendedorActionPerformed

