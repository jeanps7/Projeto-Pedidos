    private void btnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarActionPerformed
        double valorTotal = 0.0, valor;
        int qtdePedido = 0;

        for (int i = 0; i < cadPed.size(); i++) {
            if (cadPed.get(i).getVendedor().getNome().equals(lblVendedor.getText())) {
                for (int j = 0; j < cadPed.get(i).getItemPedido().size(); j++) {
                    valorTotal += cadPed.get(i).getItemPedido().get(j).getProduto().getPreco() * cadPed.get(i).getItemPedido().get(j).getQntdVendida();
                    qtdePedido++;
                }
            };
        }
        if (qtdePedido != 0) {
            lblQtdePedido.setText(String.valueOf(qtdePedido));
            valor = (((Vendedor) cadCliVend.get(posCliVenPro)).getTaxaComissao() / 100) * valorTotal;
            lblTotalTaxa.setText(String.valueOf(valor));
        } else {
            JOptionPane.showMessageDialog(null,
                    "Não há ocorrência de pedidos",
                    "Não há ocorrência de pedidos",
                    JOptionPane.WARNING_MESSAGE);
            txtCPF.setText(null);
            txtCPF.requestFocus();
        }


    }//GEN-LAST:event_btnConsultarActionPerformed

